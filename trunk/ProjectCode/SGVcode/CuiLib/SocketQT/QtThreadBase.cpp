void QtThreadBase::startServer()
{
	this->M_THREAD_RUN = true;
	this->start();
}
/*-------------------------------------*/
/**
*
*/
/*-------------------------------------*/
void QtThreadBase::closeServer()
{
	this->M_THREAD_RUN = false;

	while (this->isRunning())
	{
		QThread::sleep(1);
		std::cout <<"wait for thread done";
	}
}
