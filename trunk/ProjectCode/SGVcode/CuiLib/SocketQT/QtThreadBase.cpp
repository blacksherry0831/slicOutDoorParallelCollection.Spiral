QtThreadBase::QtThreadBase(QObject *parent):QThread(parent)
{
	this->M_THREAD_RUN = true;
	this->MAX_MSECS = 30000;
	
	QTime t;
	t = QTime::currentTime();
	qsrand(t.msec() + t.second() * 1000);

	this->IDENTIFY=qrand();
}
