/*-------------------------------------*/
int QtTcpClient::WriteMy(QByteArray _data)
{
	const int size_b_t = _data.size();

	if  (size_b_t> 0) {
	
		int size=this->write(_data.data(),size_b_t);

		this->waitForBytesWritten(MAX_MSECS);

		return size;
	}
	return 0;

	
}

